void solve(){
    ll n,m,ans=0;
    cin>>n>>m;
    ll a[n][m];
    ll dp[n+m-1][2];
    memset(dp,0,sizeof(dp));
    for (int i = 0; i < n; ++i)
    {
        for (int j = 0; j < m; ++j)
        {
            cin>>a[i][j];
            if(i+j<(n-1+m-1-i-j))
                dp[i+j][a[i][j]]++;
            else if(i+j>n+m-2-i-j)
                dp[(n-1-1-i-j+m)][a[i][j]]++;
        }
    }
    for (int i = 0; i < n+m-1; ++i)
    {
        ans+=min(dp[i][0],dp[i][1]);
    }    
    cout<<ans<<endl;
}
